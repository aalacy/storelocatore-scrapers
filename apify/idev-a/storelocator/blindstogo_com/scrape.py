proxy_country = "us"


@retry(stop=stop_after_attempt(7), wait=wait_random(min=100, max=160))
def get_data(page_url):
    global proxy_country
    while True:
        rr = random.choice(cc)
        if proxy_country == rr:
            continue
        proxy_country = rr
        break

    with SgRequests(
        proxy_country=proxy_country, retries_with_fresh_proxy_ip=7, verify_ssl=False
    ) as session:
        _headers["User-Agent"] = random.choice(user_agents)
        sp1 = bs(session.get(page_url, headers=_headers).text, "lxml")
        if not sp1.text.strip():
            raise Exception
        return sp1


def fetch_data():
    locator_domain = "https://www.blindstogo.com"
    base_url = "https://www.blindstogo.com/programs/store-finder/stores.xml"
    with SgRequests() as session:
        links = bs(session.get(base_url, headers=_headers).text, "lxml").select("store")
        logger.info(f"{len(links)} found")
        for link in links:
            page_url = link.storeurl.text
            logger.info(page_url)
            sp1 = get_data(page_url)
            if (
                sp1.select_one("h1.sppb-title-heading")
                and "Coming Soon!" in sp1.select_one("h1.sppb-title-heading").text
            ):
                continue
            if sp1.h1 and sp1.h1.text.strip().startswith("WE’VE MOVED TO"):
                page_url = (
                    locator_domain
                    + sp1.select_one(
                        "div.sppb-addon-content a.sppb-btn-success.sppb-btn-lg"
                    )["href"]
                )
                sp1 = get_data(page_url)
            location_type = ""
            _banner = sp1.select_one("a.sppb-btn-block.sppb-btn-square")
            if _banner and "temporarily closed" in _banner.text.lower():
                location_type = "Temporarily Closed"
            address = sp1.select("address")
            if not address[-1].text.strip():
                del address[-1]

            addr = [aa.replace("\xa0", " ") for aa in address[-1].stripped_strings][1:]
            if addr[-1] == "Get Directions":
                del addr[-1]
            zip_postal = " ".join(addr[-1].split(",")[1].strip().split(" ")[1:]).strip()
            if not zip_postal and len(addr[-1].split(",")) == 3:
                zip_postal = addr[-1].split(",")[-1].strip()
            country_code = "US"
            if not zip_postal.replace("-", "").isdigit() and len(zip_postal) > 5:
                country_code = "CA"
            hours = []
            _hr = sp1.find("", string=re.compile(r"Opening Hours"))
            if _hr and not (
                _hr.find_parent().find_parent().find_parent()
                and _hr.find_parent().find_parent().find_parent().find_next_sibling()
            ):
                _hr = _hr.find_parent("strong")
                temp = [
                    hh.replace("\xa0", " ")
                    for hh in _hr.find_parent().find_parent().stripped_strings
                ][1:]
                if _p(temp[-1]):
                    del temp[-1]
                day = ""
                times = []
                for x, hh in enumerate(temp):
                    if hh.split(" ")[0].split(":")[0] in days:
                        if day:
                            hours.append(f"{day} {' '.join(times)}")
                        day = hh
                        times = []
                    else:
                        times.append(hh)
                    if x == len(temp) - 1:
                        hours.append(f"{day} {' '.join(times)}")
            elif sp1.find("", string=re.compile(r"Opening Hours")):
                _hr = sp1.find("", string=re.compile(r"Opening Hours"))
                if _hr.find_parent("p"):
                    temp = list(
                        _hr.find_parent("p").find_next_sibling().stripped_strings
                    )
                    for x in range(0, len(temp), 2):
                        hours.append(f"{temp[x]} {temp[x+1]}")

            yield SgRecord(
                page_url=page_url,
                store_number=link.number.text,
                location_name=link.storename.text,
                street_address=" ".join(addr[:-1]),
                city=addr[-1].split(",")[0].strip(),
                state=addr[-1].split(",")[1].strip().split(" ")[0].strip(),
                zip_postal=zip_postal,
                country_code=country_code,
                phone=sp1.select_one("div.sppb-addon-content a")
                .text.split("ext")[0]
                .strip(),
                locator_domain=locator_domain,
                latitude=link.lat.text,
                longitude=link.lng.text,
                location_type=location_type,
                hours_of_operation="; ".join(hours).replace("–", "-"),
                raw_address=" ".join(addr),
            )


if __name__ == "__main__":
    with SgWriter(SgRecordDeduper(RecommendedRecordIds.PageUrlId)) as writer:
        results = fetch_data()
        for rec in results:
            writer.write_row(rec)
