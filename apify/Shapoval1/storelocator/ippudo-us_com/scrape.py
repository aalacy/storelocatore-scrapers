        ad = (
            " ".join(ad)
            .replace("CA94704", "CA 94704")
            .replace("San Francisco", "San Francisco, CA")
            .strip()
        )
        ad = (
            ad.replace("(Between 9th and 10th Street)", "")
            .replace("(Between 8th and 9th Avenue)", "")
            .replace("(Between 5th and 6th Avenue)", "")
        )
        a = usaddress.tag(ad, tag_mapping=tag)[0]
        street_address = f"{a.get('address1')} {a.get('address2')}".replace(
            "None", ""
        ).strip()
        state = a.get("state")
        postal = a.get("postal")
        country_code = "US"
        city = a.get("city")
        store_number = "<MISSING>"
