import re
import csv
from lxml import etree

from sgrequests import SgRequests
from sgscrape.sgpostal import parse_address_intl


def write_output(data):
    with open("data.csv", mode="w", encoding="utf-8") as output_file:
        writer = csv.writer(
            output_file, delimiter=",", quotechar='"', quoting=csv.QUOTE_ALL
        )

        # Header
        writer.writerow(
            [
                "locator_domain",
                "page_url",
                "location_name",
                "street_address",
                "city",
                "state",
                "zip",
                "country_code",
                "store_number",
                "phone",
                "location_type",
                "latitude",
                "longitude",
                "hours_of_operation",
            ]
        )
        # Body
        for row in data:
            writer.writerow(row)


def fetch_data():
    # Your scraper here
    session = SgRequests().requests_retry_session(retries=2, backoff_factor=0.3)

    items = []

    start_url = "https://kith.com/pages/locations"
    domain = re.findall(r"://(.+?)/", start_url)[0].replace("www.", "")
    hdr = {
        "user-agent": "Mozilla/5.0 (Macintosh; Intel Mac OS X 11_2_0) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/88.0.4324.150 Safari/537.36"
    }
    response = session.get(start_url, headers=hdr)
    dom = etree.HTML(response.text)

    all_locations = dom.xpath('//li[@class="page-location__location"]')
    for poi_html in all_locations:
        store_url = start_url
        location_name = poi_html.xpath(".//h2/text()")[0].strip()
        raw_address = poi_html.xpath(
            './/p[@class="page-location__location-address"]/text()'
        )
        raw_address = [e.strip() for e in raw_address]
        addr = parse_address_intl(" ".join(raw_address))
        street_address = addr.street_address_1
        if addr.street_address_2:
            street_address += " " + addr.street_address_2
        city = addr.city
        if location_name == "Brooklyn":
            city = "NY"
        state = addr.state
        state = state if state else "<MISSING>"
        zip_code = addr.postcode
        zip_code = zip_code if zip_code else "<MISSING>"
        country_code = addr.country
        country_code = country_code if country_code else "<MISSING>"
        store_number = "<MISSING>"
        raw_hoo = poi_html.xpath('.//p[@class="page-location__location-hours"]//text()')
        raw_hoo = [e.strip() for e in raw_hoo if e.strip()]
        phone = raw_hoo[-1]
        if "AM" in phone:
            phone = "<MISSING>"
        location_type = "<MISSING>"
        geo = poi_html.xpath(".//h2/a/@href")[0].split("/@")[-1].split(",")[:2]
        latitude = geo[0]
        longitude = geo[1]
        hoo = [e for e in raw_hoo if "(" not in e]
        hours_of_operation = " ".join(hoo)

        item = [
            domain,
            store_url,
            location_name,
            street_address,
            city,
            state,
            zip_code,
            country_code,
            store_number,
            phone,
            location_type,
            latitude,
            longitude,
            hours_of_operation,
        ]

        items.append(item)

    return items


def scrape():
    data = fetch_data()
    write_output(data)


if __name__ == "__main__":
    scrape()
